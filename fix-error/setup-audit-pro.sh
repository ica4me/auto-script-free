        p="${BASH_REMATCH[1]}"
        if [[ -e "$p" ]]; then
            echo "$line" >> "$TMP_RULES"
        fi
    elif [[ "$line" =~ dir=([^[:space:]]+) ]]; then
        d="${BASH_REMATCH[1]}"
        if [[ -d "$d" ]]; then
            echo "$line" >> "$TMP_RULES"
        fi
    else
        echo "$line" >> "$TMP_RULES"
    fi
done < "$AUDIT_RULES_FILE"
mv "$TMP_RULES" "$AUDIT_RULES_FILE"
