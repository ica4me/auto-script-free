set -Eeuo pipefail

ENV_FILE="/etc/system-health-monitor.env"
[[ -r "$ENV_FILE" ]] || { echo "ENV file tidak ditemukan: $ENV_FILE"; exit 1; }
# shellcheck disable=SC1090
